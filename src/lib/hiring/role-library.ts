function personas(
  title: string,
  highTitle: string,
  premiumTitle: string,
  domain: string,
  highFor: string,
  premiumFor: string,
): RoleLibraryEntry["candidatePersonas"] {
  return {
    high_capacity: {
      title: highTitle,
      strengths: ["High-volume output", "Fast first drafts", "Routine task execution", "Quick follow-ups"],
      bestFor: highFor,
    },
    recommended: {
      title,
      strengths: ["Balanced quality and capacity", "Reliable day-to-day work", "Clear communication", "Structured output"],
      bestFor: `Day-to-day ${domain} work for most teams`,
      whyRecommended: `Best balance of quality, weekly AI Work Hours, and cost for typical ${title.toLowerCase()} responsibilities.`,
    },
    premium: {
      title: premiumTitle,
      strengths: ["Deep strategic reasoning", "Complex edge cases", "Senior stakeholder ready", "Higher judgment quality"],
      bestFor: premiumFor,
    },
  };
}

type RoleSeed = Omit<RoleLibraryEntry, "departmentLabel"> & { departmentGroupId: DepartmentGroupId };

function defineRole(seed: RoleSeed): RoleLibraryEntry {
  return {
    ...seed,
    departmentLabel: GROUP_LABELS[seed.departmentGroupId],
  };
}

