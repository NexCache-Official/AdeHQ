const ROLE_BY_KEY = new Map(ROLE_ENTRIES.map((r) => [r.roleKey, r]));

export function getRoleByKey(roleKey: string | null | undefined): RoleLibraryEntry | undefined {
  if (!roleKey) return undefined;
  return ROLE_BY_KEY.get(roleKey);
}

export function getAllRoles(): RoleLibraryEntry[] {
  return ROLE_ENTRIES;
}

export function getPopularRoles(): RoleLibraryEntry[] {
  return ROLE_ENTRIES.filter((r) => r.popular);
}

export function getRolesByDepartmentGroup(groupId: DepartmentGroupId): RoleLibraryEntry[] {
  return ROLE_ENTRIES.filter((r) => r.departmentGroupId === groupId && !r.specialized);
}

export function getBrowsableDepartmentGroups(): DepartmentGroup[] {
  return DEPARTMENT_GROUPS.filter((g) => g.browseDefault);
}

export function searchRoles(query: string, limit = 8): RoleLibraryEntry[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  const scored = ROLE_ENTRIES.map((role) => {
    let score = 0;
    if (role.title.toLowerCase().includes(q)) score += 10;
    if (role.roleKey.includes(q.replace(/\s+/g, "_"))) score += 8;
    for (const alias of role.searchAliases) {
      if (alias.includes(q) || q.includes(alias)) score += 5;
    }
    for (const use of role.commonUseCases) {
      if (use.includes(q) || q.includes(use)) score += 4;
    }
    if (role.description.toLowerCase().includes(q)) score += 2;
    return { role, score };
  })
    .filter((x) => x.score > 0)
    .sort((a, b) => b.score - a.score);
  return scored.slice(0, limit).map((x) => x.role);
}

export function legacyDepartmentIdForRole(roleKey: string | null | undefined): string | null {
  return getRoleByKey(roleKey)?.legacyDepartmentId ?? null;
}

